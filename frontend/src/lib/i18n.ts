export type TranslationKey = keyof (typeof translations)["en"];
type TranslationVariables = Record<string, string | number>;

const listeners = new Set<() => void>();

function detectInitialLocale(): AppLocale {
  if (typeof window === "undefined") {
    return DEFAULT_LOCALE;
  }

  const saved = window.localStorage.getItem(LOCALE_STORAGE_KEY);
  if (saved === "en" || saved === "ru" || saved === "pt") {
    return saved;
  }

  const candidate = window.navigator.language.toLowerCase();
  if (candidate.startsWith("ru")) {
    return "ru";
  }
  if (candidate.startsWith("pt")) {
    return "pt";
  }

  return DEFAULT_LOCALE;
}

let currentLocale: AppLocale = detectInitialLocale();

function applyLocaleToDocument(locale: AppLocale) {
  if (typeof document === "undefined") {
    return;
  }

  document.documentElement.lang = locale === "pt" ? "pt-PT" : locale;
}

export function initializeI18n() {
  applyLocaleToDocument(currentLocale);
}

export function setCurrentLocale(locale: AppLocale) {
  if (typeof window !== "undefined") {
    window.localStorage.setItem(LOCALE_STORAGE_KEY, locale);
  }

  if (locale === currentLocale) {
    applyLocaleToDocument(locale);
    return;
  }

  currentLocale = locale;
  applyLocaleToDocument(locale);
  listeners.forEach((listener) => listener());
}

function subscribe(listener: () => void) {
  listeners.add(listener);
  return () => listeners.delete(listener);
}

function getSnapshot() {
  return currentLocale;
}

export function translate(key: TranslationKey, variables?: TranslationVariables, locale = currentLocale) {
  const template = String(translations[locale][key] ?? translations.en[key]);

  if (!variables) {
    return template;
  }

  return Object.entries(variables).reduce((result, [token, value]) => {
    return result.split(`{{${token}}}`).join(String(value));
  }, template);
}

export function useI18n() {
  const locale = useSyncExternalStore(subscribe, getSnapshot, getSnapshot);

  return {
    locale,
    setLocale: setCurrentLocale,
    t: (key: TranslationKey, variables?: TranslationVariables) => translate(key, variables, locale),
    localeOptions: (["en", "ru", "pt"] as AppLocale[]).map((value) => ({
      value,
      label: translate(`language.${value}` as TranslationKey, undefined, locale),
    })),
  };
}
